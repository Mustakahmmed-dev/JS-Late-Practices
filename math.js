// abs, round, ceil, floor, random, etc

// abs: Remove the Math operators and shows the gap between
console.log("Abs: ", Math.abs(5-20));

// round: takes its nearest integer
console.log("Round : ", Math.round(20.6));

// ceil: this takes to its higher level whatever the decimal number is
console.log("Ceil :", Math.ceil(5.0001));

// floor: takes its decimal number to the lowest integer
console.log("Floor: ", Math.floor(32.7));

// random: it creates random numbers
let randomNum = Math.random();
console.log("Random: ", randomNum);
